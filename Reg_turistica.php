              El edificio fue construido en el año 1918 cuando el general Manuel M. Diéguez era gobernador de Jalisco. La situación del estado cambio a un gobierno liberal y ellos pensaban que era necesario cultivar no solo la tierra sino también la mente de las autoridades que estaban a cargo de guiar a otras personas. Fue fundada como se ve ahora en 12 de octubre 1925 siendo gobernador del estado José Guadalupe Zuno. El edificio tiene la forma de una cruz con arcos de punto medio. En la parte superior y en todo el edificio hay barandillas en estilo renacentista francés. Al lado de la Rectoría hay otras oficinas. Ahora es un museo de las Artes, fundado en 1995, y cuenta con 5 habitaciones grandes. Dentro de la sala principal se encuentran dos murales de José Clemente Orozco, El &quot;Hombre Pentafásico&quot; y &quot; El Pueblo y Sus Falsos Líderes&quot;.</p>
